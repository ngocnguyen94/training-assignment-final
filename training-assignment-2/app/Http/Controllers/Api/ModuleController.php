    public function getParentModule(Request $request)
    {
        $search_term = $request->input('q');
        $id = $request->route('id');

        $result = Module::where('id', '!=', $id);

        if ($search_term) {
            $result = $result->where('name', 'iLIKE', '%'.$search_term.'%');
        }

        return $result->paginate(10);
    }
